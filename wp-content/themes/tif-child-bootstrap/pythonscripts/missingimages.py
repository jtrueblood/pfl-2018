# Helper functions for working with the data

def get_players_by_year(year):
    """Get all players from a specific year."""
    return [player for player in missing_images_data if player['year'] == str(year)]

def get_players_by_position(position):
    """Get all players of a specific position."""
    return [player for player in missing_images_data if player['position'] == position.upper()]

def get_all_years():
    """Get a sorted list of all years represented in the data."""
    years = list(set(player['year'] for player in missing_images_data))
    return sorted(years)

def get_all_positions():
    """Get a sorted list of all positions represented in the data."""
    positions = list(set(player['position'] for player in missing_images_data))
    return sorted(positions)

def get_player_by_id(player_id):
    """Get a specific player by their ID."""
    for player in missing_images_data:
        if player['player_id'] == player_id:
            return player
    return None

def get_summary_stats():
    """Get summary statistics about the missing images data."""
    total_players = len(missing_images_data)
    years = get_all_years()
    positions = get_all_positions()
    
    year_counts = {}
    for year in years:
        year_counts[year] = len(get_players_by_year(year))
    
    position_counts = {}
    for position in positions:
        position_counts[position] = len(get_players_by_position(position))
    
    return {
        'total_players': total_players,
        'years_span': f"{years[0]}-{years[-1]}",
        'total_years': len(years),
        'total_positions': len(positions),
        'players_by_year': year_counts,
        'players_by_position': position_counts
    }

# Example usage:
